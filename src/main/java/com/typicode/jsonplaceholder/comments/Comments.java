package com.typicode.jsonplaceholder.comments;

public class Comments {

    private double postId;
    private double id;
    private String name;
    private String email;
    private String body;

    public double getPostId() {
        return postId;
    }

    public double getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getEmail() {
        return email;
    }

    public String getBody() {
        return body;
    }
}
